        // GET: Users/Details/5
        public ActionResult Details(int? id)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.Users.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }
            return View(user);
        }





        // GET: Users/Details/5
        public ActionResult SpecialDetails(int? id, int? idT)
        {
            if (id == null)
            {
                return new HttpStatusCodeResult(HttpStatusCode.BadRequest);
            }
            User user = db.Users.Find(id);
            if (user == null)
            {
                return HttpNotFound();
            }

            Session["t"] = idT;

            int points;
            if(user.Answers.Count() == 0)
            {
                points = 0;
            }
            else
            {
                points = db.Answers.Where(p => p.UserID == id).Sum(p => p.Points);
            }
            
            ViewBag.Points = points;

            return View(user);
        }






